package hometask;

class Employee{   
     private String name;   
     private int year, salary;   
     public Employee(String n,int y, int s){     
         name = n;     
         year = y;     
         salary = s;   }   
         public String getName(){     
             return name;   
         }   
         public int getYear(){     
             return year;   
         }   
         public int getSalary(){     
             return salary;   
         } 
 }  
    